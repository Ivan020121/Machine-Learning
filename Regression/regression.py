def regularize(xMat):
    inMat = xMat.copy()
    # 计算平均值，然后减去它
    inMeans = mean(inMat,0)
    # Xi的Calc方差，然后除以席
    inVar = var(inMat,0)
    inMat = (inMat - inMeans)/inVar
    return inMat

